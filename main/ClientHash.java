public class ClientHash {
    private String hash;
    private String prevHash;

    public void setHash(String hash) {
        this.hash = hash;
    }

    public String getHash() {
        return this.hash;
    }

    public void setPrevHash(String hash) {
        this.prevHash = hash;
    }

    public String getPrevHash() {
        return this.prevHash;
    }
}
